package com.i8web.entity.Client;

import java.text.NumberFormat;
import java.util.Locale;

public class Products {
	private int id;
	private String name;
	private String slug;
	private String price_old;
	private String price_new;
	private String description;
	private String status;
	private String many_image;
	private String image;
	private String detail;
	private int product_id;
	private String ghimSale;
	private String ghimNew;
	private String created_at;
	public Products() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSlug() {
		return slug;
	}
	public void setSlug(String slug) {
		this.slug = slug;
	}
	public String getPrice_old() {
		Locale localeVN = new Locale("vi", "VN");
		double test = Double.parseDouble(price_old); 
		NumberFormat currencyVN = NumberFormat.getInstance(localeVN);;
		return currencyVN.format(test) + "Đ";
	}
	public void setPrice_old(String price_old) {
		this.price_old = price_old;
	}
	public String getPrice_new() {
		Locale localeVN = new Locale("vi", "VN");
		double test = Double.parseDouble(price_new); 
		NumberFormat currencyVN = NumberFormat.getInstance(localeVN);;
		return currencyVN.format(test) + "Đ";
	}
	public String getPriceNewCheckout() {
		return this.price_new;
	}
	public void setPrice_new(String price_new) {
		this.price_new = price_new;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMany_image() {
		return many_image;
	}
	public void setMany_image(String many_image) {
		this.many_image = many_image;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public int getProduct_id() {
		return product_id;
	}
	public void setProduct_id(int product_id) {
		this.product_id = product_id;
	}
	public String getGhimSale() {
		return ghimSale;
	}
	public void setGhimSale(String ghimSale) {
		this.ghimSale = ghimSale;
	}
	public String getGhimNew() {
		return ghimNew;
	}
	public void setGhimNew(String ghimNew) {
		this.ghimNew = ghimNew;
	}
	public String getCreated_at() {
		return created_at;
	}
	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}

	
	
}
